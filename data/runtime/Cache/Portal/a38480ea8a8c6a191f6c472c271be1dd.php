            <p>武汉盛帆电子股份有限公司（简称“盛帆股份”）创立于1998年，是国家火炬计划重点高新技术企业和国家电网公司配套重点骨干企业，位于中国光谷——风景秀丽的汤逊湖畔，占地12万平方米。盛帆股份是中国领先的专业水、电、气、热、节能监测管理方案提供商，拥有完整的产品线和灵活的解决方案，能够满足不同水电气公司及高校、政府机关等大型企事业单位的差异化需求。</p>
            <p>在“节能、环保、创新”的经营理念下，盛帆股份坚持以技术创新为客户创造价值，先后成功研制出各类小体积、低功耗产品50余种；共取得国家专利和软件著作权100余项（其中发明专利9项），在同行业中处于领先地位；在国内首创自主低功耗设计方案，使电能表的功耗全面降低至0.3W（为普通电能表35%左右），为国家节约大量能源，取得了巨大的社会效益。“低功耗智能电能表”新技术经过国家电网公司多轮专家评估，被纳入《国家电网公司新技术目录(2014版)》。依照国务院节能减排“十二五”规划精神开发出的“能耗资源管理系统”，能够对水、电、气、热量等数据进行实时监控采集与分析，为解决大型企事业单位的公共设备节能提供了必备的技术支撑。凭借突出的创新能力，公司产品被国家科学技术部、国家环境保护部、国家商务部、国家质量监督检验检疫总局评为“国家重点新产品”，获得国家知识产权局颁发的“中国外观设计优秀奖”，多次被评为“中国优质产品”、“湖北名牌产品”。公司被列为“湖北省创新型企业试点单位”、“湖北省企业技术中心”、“湖北省工程技术研究中心”，取得十余项省、市级科技成果认定，并获得“湖北省科技进步奖”、“武汉市科技型中小企业创新奖”、“武汉市科技成果转化重大贡献奖”等国家、湖北省和武汉市30余项奖励和项目支持。公司还被世界自然基金会评为“2014年WWF气候创行者”，这是低碳领域的世界性荣誉，全国仅五家企业入选。</p>
            <p>对盛帆人而言，盛帆是创业的起点、发展的阶梯、理想的舞台。作为主要股东来自周总理母校---南开大学的企业，盛帆股份取南开和清华的校训“允公允能、厚德载物”为公司核心价值观，公司股权对所有认同公司核心价值观的法人和个人开放。公司绝大多数重要岗位员工都持有公司股权，新三板挂牌确认的股东人数为118名。</p>
            <p>在关注自身发展的同时，盛帆股份积极承担社会责任，始终把关心和支持社会公益事业作为己任，公司注资1000万元设立了盛帆公益基金，专门用于奖学助学、捐资修路等社会公益事业。其中南开大学总额500万元捐助、湖北省仙桃中学百万奖学金、仙桃市修建的盛帆桥、嘉鱼县铺设的盛帆路等专项捐助都取得了良好的社会效益。对社会弱势群体公司同样关注：2008年公司积极组织对汶川灾区捐款,2013、2014年联合楚天都市报为300多名环卫工人发放过年物资，为福利院的老人们送去过冬保暖物资，为留守儿童圆梦……从2013年开始，公司每年将利润的3%（超过200万元）用于各类公益活动，并逐步提高该比例。</p>
